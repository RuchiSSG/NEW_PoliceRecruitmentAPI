                parameters.Add("@tbl_RFIDUploadType", user.DataTable.AsTableValuedParameter("[dbo].[tbl_RFIDUploadType]"));
            }
            parameters.Add("@OutcomeId", dbType: DbType.Int32, direction: ParameterDirection.Output);
