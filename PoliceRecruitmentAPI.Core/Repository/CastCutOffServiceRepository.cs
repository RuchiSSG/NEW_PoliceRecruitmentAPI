        public DynamicParameters SetCandidate(CastCutOffServiceDto user)
        {
            DynamicParameters parameters = new DynamicParameters();
            parameters.Add("@OperationType", user.BaseModel.OperationType, DbType.String);
            parameters.Add("@UserId", user.UserId, DbType.String);
            parameters.Add("@CategoryId", user.CategoryId, DbType.String);
            parameters.Add("@CategoryName", user.CategoryName, DbType.String);
            parameters.Add("@Selectionratio", user.Selectionratio, DbType.String);
            parameters.Add("@Groundtestcutoff", user.Groundtestcutoff, DbType.String);
            parameters.Add("@Writtentestcutoff", user.Writtentestcutoff, DbType.String);
            parameters.Add("@RecruitId", user.RecruitId, DbType.String);
            parameters.Add("@isActive", user.IsActive, DbType.String);
            if (user.DataTable != null && user.DataTable.Rows.Count > 0)
            {
                parameters.Add("@CastCut", user.DataTable.AsTableValuedParameter("[dbo].[tbl_CastCutOffMaster]"));
            }
            parameters.Add("@OutcomeId", dbType: DbType.Int32, direction: ParameterDirection.Output);
            parameters.Add("@OutcomeDetail", dbType: DbType.String, size: 4000, direction: ParameterDirection.Output);
            return parameters;

        }
